#include <iostream>
using namespace std;
int main()
{
    cout<<"sizeof(bool)= "<<sizeof(bool)<<" byte"<<endl<<endl;

    cout<<"sizeof(char)= "<<sizeof(char)<<" byte"<<endl<<endl;

    cout<<"sizeof(short)= "<<sizeof(short)<<" byte"<<endl<<endl;

    cout<<"sizeof(int)= "<<sizeof(int)<<" byte"<<endl<<endl;

    cout<<"sizeof(long)= "<<sizeof(long)<<" byte"<<endl<<endl;

    cout<<"sizeof(long long)= "<<sizeof(long long)<<" byte"<<endl<<endl;

    cout<<"sizeof(float)= "<<sizeof(float)<<" byte"<<endl<<endl;

    cout<<"sizeof(double)= "<<sizeof(double)<<" byte"<<endl<<endl;

    cout<<"sizeof(long double)= "<<sizeof(long double)<<" byte"<<endl<<endl;

    return 0;
}
