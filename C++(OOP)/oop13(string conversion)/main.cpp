#include <iostream>
#include <cstdlib>

using namespace std;
int main()
{
    char g[]="1500";
    int f=atoi(g)+5;
    cout<<f;   //1505
    return 0;
}
/*
atoi(g) covert from string to int

atof(g) covert from string to float

atol(g) covert from string to long

atoll(g) covert from string to long long
*/
